function filterEvents() {
    var searchVal = document.getElementById("searchInput") ? document.getElementById("searchInput").value.toLowerCase() : "";
    var categoryVal = document.getElementById("categoryFilter") ? document.getElementById("categoryFilter").value : "";
    var dateVal = document.getElementById("dateFilter") ? document.getElementById("dateFilter").value : "";

    var cards = document.getElementsByClassName("event-card-wrapper");

    for (var i = 0; i < cards.length; i++) {
        var card = cards[i];
        var title = card.getElementsByClassName("card-title")[0] ? card.getElementsByClassName("card-title")[0].textContent.toLowerCase() : "";
        var category = card.getAttribute("data-category") || "";
        var date = card.getAttribute("data-date") || "";

        var matchSearch = (searchVal === "" || title.indexOf(searchVal) !== -1);
        var matchCategory = (categoryVal === "" || category === categoryVal);
        var matchDate = (dateVal === "" || date === dateVal);

        card.style.display = (matchSearch && matchCategory && matchDate) ? "block" : "none";
    }
}

function resetFilter() {
    if (document.getElementById("searchInput")) document.getElementById("searchInput").value = "";
    if (document.getElementById("categoryFilter")) document.getElementById("categoryFilter").value = "";
    if (document.getElementById("dateFilter")) document.getElementById("dateFilter").value = "";
    filterEvents();
}

function submitContactForm() {
    var name = document.getElementById("userName").value.trim();
    var email = document.getElementById("userEmail").value.trim();
    var message = document.getElementById("userMessage").value.trim();
    var agree = document.getElementById("agreeTerms") ? document.getElementById("agreeTerms").checked : true;

    var successAlert = document.getElementById("successAlert");
    var errorAlert = document.getElementById("errorAlert");
    
    if (successAlert) successAlert.style.display = "none";
    if (errorAlert) errorAlert.style.display = "none";

    if (name.length < 3) { showError("الاسم قصير جداً"); return; }
    if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email)) { showError("البريد غير صحيح"); return; }
    if (message.length < 10) { showError("الرسالة قصيرة جداً"); return; }
    if (!agree) { showError("يرجى الموافقة على الشروط"); return; }

    if (successAlert) {
        successAlert.textContent = "تم استلام رسالتك يا " + name + " ✅";
        successAlert.style.display = "block";
        resetForm();
    }
}

function showError(msg) {
    var errorAlert = document.getElementById("errorAlert");
    if (errorAlert) { errorAlert.textContent = msg; errorAlert.style.display = "block"; }
}

function resetForm() {
    ["userName", "userEmail", "messageSubject", "userMessage"].forEach(id => {
        if (document.getElementById(id)) document.getElementById(id).value = "";
    });
}

function startEventCountdown(dateString) {
    const targetDate = new Date(dateString.replace(/\//g, '-')).getTime();
    setInterval(function() {
        const now = new Date().getTime();
        const diff = targetDate - now;
        if (document.getElementById("days")) {
            document.getElementById("days").innerText = Math.floor(diff / (1000 * 60 * 60 * 24));
            document.getElementById("hours").innerText = Math.floor((diff % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
            document.getElementById("minutes").innerText = Math.floor((diff % (1000 * 60 * 60)) / (1000 * 60));
            document.getElementById("seconds").innerText = Math.floor((diff % (1000 * 60)) / 1000);
        }
    }, 1000);
}

function confirmBooking(eventName) {
    if (document.getElementById('modal-event-name')) document.getElementById('modal-event-name').innerText = eventName;
    if (document.getElementById('bookingModal')) new bootstrap.Modal(document.getElementById('bookingModal')).show();
}

function shareEvent(title) {
    if (navigator.share) navigator.share({ title: title, url: window.location.href });
    else alert("تم نسخ رابط: " + title);
}

const themeToggler = document.getElementById('themeToggler');
if (themeToggler) {
    const applyTheme = (theme) => {
        document.documentElement.setAttribute('data-bs-theme', theme);
        localStorage.setItem('theme', theme);
        const icon = document.getElementById('themeIcon');
        if (icon) icon.className = theme === 'dark' ? 'bi bi-sun-fill' : 'bi bi-moon-fill';
    };
    applyTheme(localStorage.getItem('theme') || 'light');
    themeToggler.onclick = () => applyTheme(document.documentElement.getAttribute('data-bs-theme') === 'light' ? 'dark' : 'light');
}

document.addEventListener("DOMContentLoaded", function() {
    const urlParams = new URLSearchParams(window.location.search);
    const catParam = urlParams.get('cat');
    const categoryFilter = document.getElementById("categoryFilter");

    if (catParam && categoryFilter) {
        categoryFilter.value = catParam;
        filterEvents();
    }

    const eventId = urlParams.get('id');

    var currentPage = window.location.pathname.split("/").pop();
    var navLinks = document.getElementsByClassName("nav-link");
    for (var i = 0; i < navLinks.length; i++) {
        if (navLinks[i].getAttribute("href") === currentPage) navLinks[i].classList.add("active");
    }

    if (eventId && eventsData[eventId]) {
        const ev = eventsData[eventId];
        if (document.getElementById('event-title')) document.getElementById('event-title').innerText = ev.title;
        if (document.getElementById('event-image')) document.getElementById('event-image').style.backgroundImage = `url('${ev.image}')`;
        if (document.getElementById('event-description')) document.getElementById('event-description').innerHTML = ev.description;
        if (document.getElementById('sidebar-time')) document.getElementById('sidebar-time').innerText = ev.time;
        if (document.getElementById('sidebar-city')) document.getElementById('sidebar-city').innerText = ev.city;
        if (document.getElementById('sidebar-location')) document.getElementById('sidebar-location').innerText = ev.sidebarLocation;
        if (document.getElementById('sidebar-attendance')) document.getElementById('sidebar-attendance').innerText = ev.attendance;

        startEventCountdown(ev.headerDate);

        const galleryContainer = document.getElementById('event-gallery');
        if (galleryContainer && ev.gallery) {
            galleryContainer.innerHTML = ev.gallery.map(img => `
                <div class="col-4">
                    <div class="gallery-item" style="background-image: url('${img}');"></div>
                </div>`).join('');
        }
        
        if (document.getElementById('btn-book')) document.getElementById('btn-book').onclick = () => confirmBooking(ev.title);
        if (document.getElementById('btn-share')) document.getElementById('btn-share').onclick = () => shareEvent(ev.title);
    }
});

